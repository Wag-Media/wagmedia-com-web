export const countryFlags = Object.keys(countries).map((code) => {
  return {
    code,
    name: countries[code][0],
    language: countries[code][1]?.toLowerCase(),
    flag: flag(code),
  }
})

export const getFlagByLanguage = (language: string) => {
  if (!language) return
  if (language === "czech") language = "czech republic"
  const country = countryFlags.find((country) => country.language === language)
  return country?.flag
}
